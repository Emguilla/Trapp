function Rx=rotx(theta)
%==================================================================================================================================%
% rotx.m:   calculation of the rotation matrix around the x-axis (v0.1)
%==================================================================================================================================%
% Version history:
%   version 0.1 (25/08/2025) - Creation
%       author: EYG
%==================================================================================================================================%
% args:
%   theta:  rotation angle /!\ in radian /!\
%==================================================================================================================================%
    Rx=[ 1           0           0;
         0           cos(theta) -sin(theta);
         0           sin(theta)  cos(theta)];
end
