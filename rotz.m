function Rz=rotz(theta)
%==================================================================================================================================%
% rotz.m:   calculation of the rotation matrix around the z-axis (v0.1)
%==================================================================================================================================%
% Version history:
%   version 0.1 (25/08/2025) - Creation
%       author: EYG
%==================================================================================================================================%
% args:
%   theta:  rotation angle /!\ in radian /!\
%==================================================================================================================================%
    Rz=[ cos(theta) -sin(theta)  0;
         sin(theta)  cos(theta)  0;
         0           0           1];
end
