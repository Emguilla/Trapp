function E=ZPE(nu)
%==================================================================================================================================%
% ZPE.m:    computation of the zero-point energy of a system using the frequencies of its vibrational mode
%==================================================================================================================================%
% Version history:
%   version 0.1 (14/08/2025) - Creation
%       author: EYG
%==================================================================================================================================%
% args:
%   nu: Array of frequencies
%==================================================================================================================================%
load('constant_fund.mat','h')
E=0.5*h*sum(nu);
end