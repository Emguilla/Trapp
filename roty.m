function Ry=roty(theta)
%==================================================================================================================================%
% roty.m:   calculation of the rotation matrix around the y-axis (v0.1)
%==================================================================================================================================%
% Version history:
%   version 0.1 (25/08/2025) - Creation
%       author: EYG
%==================================================================================================================================%
% args:
%   theta:  rotation angle /!\ in radian /!\
%==================================================================================================================================%
    Ry=[ cos(theta)  0           sin(theta);
         0           1           0;
        -sin(theta)  0           cos(theta)];
end
